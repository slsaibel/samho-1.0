    public void carregarObjetoIFrmManutencao(Telefones telefone) {
        try {
            if (telefone.getIdTelefone() != -1) {
                txtCodigo.setText(String.valueOf(telefone.getIdTelefone()));
            } else {
                txtCodigo.setText(String.valueOf(
                        telefone.getTelefonesDAO().getProximoID()));
            }

            txtCodTipoTelefone.setText(String.valueOf(telefone.getCodTipoTelefone()));
            pesquisarCodigoDescicaoTipoTelefone(new TiposTelefones());

            txtNumero.setText(String.valueOf(telefone.getNumero()));
            txtRamal.setText(String.valueOf(telefone.getRamal()));
            txtContato.setText(telefone.getContato());
            chkAtivo.setSelected(telefone.isAtivo());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this.getParent(),
                    "Registro nao pode ser carregado.\n\nMotivo: " + ex);
        }
    }

    private void reescreverObjeto() {
        try {
            telefone.setIdTelefone(Long.parseUnsignedLong(txtCodigo.getText()));
            telefone.setCodTipoTelefone(Long.parseUnsignedLong(
                    txtCodTipoTelefone.getText()));
            telefone.setNumero(Long.parseUnsignedLong(
                    Formatacao.removerFormatacao(txtNumero.getText())));
            telefone.setRamal(Long.parseUnsignedLong(txtRamal.getText()));
            telefone.setContato(txtContato.getText());
            telefone.setAtivo(chkAtivo.isSelected());

            telefone.adicionarCampos();
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this.getParent(),
                    "Registro nao pode ser reescrito.\n\nMotivo: " + ex);
        }
    }

    public void pesquisarCodigoDescicaoTipoTelefone(TiposTelefones tipoTelefone) {
        Util.pesquisarCodigoDescicao(
                tipoTelefone, txtCodTipoTelefone, txtDescrTipoTelefone);
    }

