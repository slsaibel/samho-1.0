    private void lblLocalizarTipoTelefoneMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblLocalizarTipoTelefoneMouseReleased
        boolean[] acoes = Util.getAcoesUsuario(Principal.TIPOS_TELEFONES);

        if (!acoes[Util.CONSULTAR]) {
            JOptionPane.showMessageDialog(this.getParent(),
                    "Você não pode efetuar esta consulta.\n\nMotivo: "
                            + "Usuário sem permissão.");
        } else {         
            try {
                IFrmPesqTiposTelefones iFrmPesqTiposTelefones
                        = new IFrmPesqTiposTelefones(new IFrmManTipoTelefone(
                                        new TiposTelefones()), txtCodTipoTelefone,
                                txtDescrTipoTelefone);

                this.getParent().remove(iFrmPesqTiposTelefones);
                this.getParent().add(iFrmPesqTiposTelefones);

                Util.centralizar(this.getParent(), iFrmPesqTiposTelefones);

                iFrmPesqTiposTelefones.setVisible(true);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this.getParent(),
                        "Tipo de endereço não pode ser consultada.\n\nMotivo: " + e);
            }
        }
    }//GEN-LAST:event_lblLocalizarTipoTelefoneMouseReleased

