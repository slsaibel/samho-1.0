    public void carregarObjetoIFrmManutencao(AcoesUsuarios acoesUsuario) {
        try {
            if (acoesUsuario.getIdAcaoUsuario() != -1) {
                txtCodigo.setText(String.valueOf(acoesUsuario.getIdAcaoUsuario()));
            } else {
                txtCodigo.setText(String.valueOf(
                        acoesUsuario.getAcaoUsuarioDAO().getProximoID()));
            }

            txtCodAcao.setText(String.valueOf(acoesUsuario.getCodAcao()));
            pesquisarCodigoDescicaoAcao(new Acoes());
            
            txtCodUsuario.setText(String.valueOf(acoesUsuario.getCodUsuario()));
            pesquisarCodigoDescicaoUsuario(new Usuarios());

            chkIncluir.setSelected(acoesUsuario.isIncluir());
            chkAlterar.setSelected(acoesUsuario.isAlterar());
            chkExcluir.setSelected(acoesUsuario.isExcluir());
            chkConsultar.setSelected(acoesUsuario.isConsultar());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this.getParent(),
                    "Registro nao pode ser carregado.\n\nMotivo: " + ex);
        }
    }

    private void reescreverObjeto() {
        try {
            acoesUsuario.setIdAcaoUsuario(Long.parseUnsignedLong(
                    txtCodigo.getText()));
            acoesUsuario.setCodAcao(Long.parseUnsignedLong(
                    txtCodAcao.getText()));
            acoesUsuario.setCodUsuario(Long.parseUnsignedLong(
                    txtCodUsuario.getText()));
            acoesUsuario.setIncluir(chkIncluir.isSelected());
            acoesUsuario.setAlterar(chkAlterar.isSelected());
            acoesUsuario.setExcluir(chkExcluir.isSelected());
            acoesUsuario.setConsultar(chkConsultar.isSelected());

            acoesUsuario.adicionarCampos();
            
            // Atualiza as ações permitidas ao usuario
            UsuariosDAO.verificaAcoesUsuario();
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this.getParent(),
                    "Registro nao pode ser reescrito.\n\nMotivo: " + ex);
        }
    }

    public void pesquisarCodigoDescicaoUsuario(Usuarios usuario) {
        Util.pesquisarCodigoDescicao(usuario, txtCodUsuario, txtDescrUsuario);
    }
    
