    private void lblLocalizarAcaoMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblLocalizarAcaoMouseReleased
        boolean[] acoes = Util.getAcoesUsuario(Principal.ACOES);
            
        if (!acoes[Util.CONSULTAR]) {
            JOptionPane.showMessageDialog(this.getParent(),
                    "Você não pode efetuar esta consultadas.\n\nMotivo: "
                            + "Usuário sem permissão.");
        } else {
            try {
                IFrmPesqAcoes iFrmPesqAcoes = new IFrmPesqAcoes(
                        new IFrmManAcao (new Acoes()), txtCodAcao, txtDescrAcao);

                this.getParent().remove(iFrmPesqAcoes);
                this.getParent().add(iFrmPesqAcoes);

                Util.centralizar(this.getParent(), iFrmPesqAcoes);

                iFrmPesqAcoes.setVisible(true);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this.getParent(),
                        "Ações não podem ser consultadas.\n\nMotivo: " + e);
            }
        }
    }//GEN-LAST:event_lblLocalizarAcaoMouseReleased

